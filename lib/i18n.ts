export function isLocale(value: unknown): value is Locale {
  return value === 'id' || value === 'en';
}

export function packageLabel(code: PackageCode | '', fallback: string, locale: Locale): string {
  if (!code) return fallback;
  const labels: Record<Locale, Record<PackageCode, string>> = {
    id: {
      HM: 'Hemat',
      RG: 'Reguler',
      ST: 'Sultan',
      RL: 'Reguler Live',
      SL: 'Sultan Live',
    },
    en: {
      HM: 'Budget',
      RG: 'Regular',
      ST: 'Sultan',
      RL: 'Regular Live',
      SL: 'Sultan Live',
    },
  };
  return labels[locale][code];
}

export function parentsLabel(value: ParentsContent, locale: Locale): string {
  const labels: Record<Locale, Record<ParentsContent, string>> = {
    id: {
      none: 'Tanpa orang tua',
      single_mom: 'Single Mom',
      single_father: 'Single Father',
      mom_and_dad: 'Mom & Dad',
    },
    en: {
      none: 'No parents',
      single_mom: 'Single Mom',
      single_father: 'Single Father',
      mom_and_dad: 'Mom & Dad',
    },
  };
  return labels[locale][value];
}

export function birthdayAge(value: string, locale: Locale): string {
  return locale === 'en' ? `${value} years old` : `${value} tahun`;
}
